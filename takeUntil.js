const takeUntil = function (array, callback) {
  const temp = [];
  for (const item in array) {
    if (!callback(array[item])) {
      temp.push(array[item]);
    } else {
      return temp;
    }
  }
};
const eqArrays = function (arr1, arr2) {
  if (arr1.length === arr2.length) {
    for (let i = 0; i < arr1.length; i++) {
      if (arr1[i] !== arr2[i]) {
        return false;
      }
    }
  } else {
    return false;
  }
  return true;

};
